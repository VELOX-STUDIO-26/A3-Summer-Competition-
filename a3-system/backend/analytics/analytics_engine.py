# Cache duration - insights are regenerated after this period
INSIGHTS_CACHE_DURATION_HOURS = 24  # Regenerate once per day

logger = get_logger(__name__)


@dataclass
class BehavioralData:
    """Aggregated behavioral data for a student."""
    student_id: str
    
    # Study patterns
    study_sessions: List[Dict[str, Any]]  # [{date, start_time, duration, topic}]
    preferred_study_hours: List[int]  # Hours of day (0-23)
    study_days_pattern: Dict[str, int]  # {Mon: 3, Tue: 1, ...}
    avg_session_duration: float  # minutes
    
    # Quiz performance
    quiz_attempts: List[Dict[str, Any]]  # [{topic, score, time_taken, weak_topics}]
    avg_quiz_score: float
    score_trend: str  # "improving", "declining", "stable"
    weak_topics: List[str]
    strong_topics: List[str]
    
    # Resource engagement
    resources_by_type: Dict[str, int]  # {notes: 5, video: 3, code: 2}
    total_resources_consumed: int
    preferred_resource_type: str
    
    # Tutor interactions
    tutor_sessions_count: int
    avg_messages_per_session: float
    common_question_topics: List[str]
    
    # Progress metrics
    path_completion_pct: float
    current_streak: int
    longest_streak: int
    days_since_last_activity: int
    
    # Velocity
    nodes_completed_last_7_days: int
    nodes_completed_last_30_days: int
    estimated_completion_days: Optional[int]


@dataclass
class LLMInsight:
    """Structured insight generated by the LLM."""
    category: str  # "pattern", "prediction", "recommendation", "alert"
    emoji: str
    title: str
    description: str
    confidence: float  # 0.0-1.0
    actionable: bool
    action_text: Optional[str] = None
    priority: str = "medium"  # "high", "medium", "low"


