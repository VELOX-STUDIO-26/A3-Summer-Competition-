    async def _aggregate_behavioral_data(
        self,
        student_id: str,
        db: AsyncSession,
    ) -> BehavioralData:
        """Aggregate all behavioral data for a student."""
        
        # Get quiz attempts
        attempts_result = await db.execute(
            select(QuizAttempt).where(
                QuizAttempt.student_id == student_id,
                QuizAttempt.score.is_not(None),
            ).order_by(QuizAttempt.completed_at.desc())
        )
        attempts = attempts_result.scalars().all()
        
        # Get learning events
        events_result = await db.execute(
            select(LearningEvent).where(
                LearningEvent.student_id == student_id,
            ).order_by(LearningEvent.created_at.desc())
        )
        events = events_result.scalars().all()
        
        # Get chat sessions (tutor interactions) - just count them, don't load messages
        sessions_result = await db.execute(
            select(ChatSession).where(
                ChatSession.student_id == student_id,
            )
        )
        sessions = sessions_result.scalars().all()
        
        # Count messages separately to avoid lazy loading issues
        from sqlalchemy import func as sql_func
        from models.database import ChatMessage
        message_count_result = await db.execute(
            select(sql_func.count(ChatMessage.message_id)).where(
                ChatMessage.session_id.in_([s.session_id for s in sessions])
            )
        ) if sessions else None
        total_message_count = message_count_result.scalar() if message_count_result else 0
        
        # Get learning path
        path_result = await db.execute(
            select(LearningPath).where(
                LearningPath.student_id == student_id,
                LearningPath.status == "active",
            )
        )
        path = path_result.scalar_one_or_none()
        
        # Get generated resources consumed
        resources_result = await db.execute(
            select(GeneratedResource).where(
                GeneratedResource.student_id == student_id,
            )
        )
        resources = resources_result.scalars().all()
        
        # Calculate study patterns
        study_sessions = self._extract_study_sessions(attempts, events)
        preferred_hours = self._calculate_preferred_hours(study_sessions)
        study_days = self._calculate_study_days(study_sessions)
        
        # Calculate quiz metrics
        quiz_data = self._analyze_quiz_performance(attempts)
        
        # Calculate resource engagement
        resource_stats = self._analyze_resource_engagement(resources, events)
        
        # Calculate tutor stats
        tutor_stats = self._analyze_tutor_sessions(sessions, total_message_count)
        
        # Calculate progress metrics
        progress = self._calculate_progress_metrics(attempts, path)
        
        # Calculate velocity
        velocity = self._calculate_learning_velocity(attempts, path)
        
        return BehavioralData(
            student_id=student_id,
            study_sessions=study_sessions[:20],  # Last 20 sessions
            preferred_study_hours=preferred_hours,
            study_days_pattern=study_days,
            avg_session_duration=self._avg_session_duration(study_sessions),
            quiz_attempts=quiz_data["attempts"],
            avg_quiz_score=quiz_data["avg_score"],
            score_trend=quiz_data["trend"],
            weak_topics=quiz_data["weak_topics"],
            strong_topics=quiz_data["strong_topics"],
            resources_by_type=resource_stats["by_type"],
            total_resources_consumed=resource_stats["total"],
            preferred_resource_type=resource_stats["preferred"],
            tutor_sessions_count=tutor_stats["count"],
            avg_messages_per_session=tutor_stats["avg_messages"],
            common_question_topics=tutor_stats["topics"],
            path_completion_pct=progress["completion_pct"],
            current_streak=progress["current_streak"],
            longest_streak=progress["longest_streak"],
            days_since_last_activity=progress["days_inactive"],
            nodes_completed_last_7_days=velocity["last_7_days"],
            nodes_completed_last_30_days=velocity["last_30_days"],
            estimated_completion_days=velocity["estimated_days"],
        )
    
    def _extract_study_sessions(
        self,
        attempts: List[QuizAttempt],
        events: List[LearningEvent],
    ) -> List[Dict[str, Any]]:
        """Extract study sessions from quiz attempts and events."""
        sessions = []
        
        for attempt in attempts:
            if attempt.completed_at:
                sessions.append({
                    "date": attempt.completed_at.strftime("%Y-%m-%d"),
                    "start_time": attempt.completed_at.strftime("%H:%M"),
                    "hour": attempt.completed_at.hour,
                    "duration": (attempt.time_spent_seconds or 0) // 60,
                    "type": "quiz",
                    "topic": attempt.quiz_id,  # Would need to join for actual topic
                })
        
        for event in events:
            if event.created_at and event.event_type in ["resource_consumed", "resource_viewed"]:
                duration = event.event_data.get("duration_seconds", 0) // 60 if event.event_data else 0
                sessions.append({
                    "date": event.created_at.strftime("%Y-%m-%d"),
                    "start_time": event.created_at.strftime("%H:%M"),
                    "hour": event.created_at.hour,
                    "duration": duration,
                    "type": event.event_type,
                    "topic": event.event_data.get("topic", "unknown") if event.event_data else "unknown",
                })
        
        # Sort by date descending
        sessions.sort(key=lambda x: x["date"], reverse=True)
        return sessions
    
    def _calculate_preferred_hours(self, sessions: List[Dict]) -> List[int]:
        """Find the most common study hours."""
        hour_counts: Dict[int, int] = {}
        for s in sessions:
            hour = s.get("hour", 12)
            hour_counts[hour] = hour_counts.get(hour, 0) + 1
        
        # Return top 3 hours
        sorted_hours = sorted(hour_counts.items(), key=lambda x: x[1], reverse=True)
        return [h for h, _ in sorted_hours[:3]]
    
    def _calculate_study_days(self, sessions: List[Dict]) -> Dict[str, int]:
        """Count study sessions by day of week."""
        days = {"Mon": 0, "Tue": 0, "Wed": 0, "Thu": 0, "Fri": 0, "Sat": 0, "Sun": 0}
        day_names = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"]
        
        for s in sessions:
            try:
                date = datetime.strptime(s["date"], "%Y-%m-%d")
                day_name = day_names[date.weekday()]
                days[day_name] += 1
            except (ValueError, KeyError):
                pass
        
        return days
    
    def _avg_session_duration(self, sessions: List[Dict]) -> float:
        """Calculate average session duration in minutes."""
        durations = [s.get("duration", 0) for s in sessions if s.get("duration", 0) > 0]
        return round(sum(durations) / len(durations), 1) if durations else 0
    
    def _analyze_quiz_performance(self, attempts: List[QuizAttempt]) -> Dict[str, Any]:
        """Analyze quiz performance patterns."""
        if not attempts:
            return {
                "attempts": [],
                "avg_score": 0,
                "trend": "stable",
                "weak_topics": [],
                "strong_topics": [],
            }
        
        # Build attempts list
        attempt_data = []
        topic_scores: Dict[str, List[float]] = {}
        
        for a in attempts:
            score = a.score or 0
            topic = a.quiz_id  # Simplified - would join for actual topic
            
            attempt_data.append({
                "topic": topic,
                "score": round(score * 100, 1),
                "time_taken": a.time_spent_seconds or 0,
                "date": a.completed_at.strftime("%Y-%m-%d") if a.completed_at else "",
            })
            
            if topic not in topic_scores:
                topic_scores[topic] = []
            topic_scores[topic].append(score)
        
        # Calculate average
        avg_score = round(sum(a.score or 0 for a in attempts) / len(attempts) * 100, 1)
        
        # Calculate trend (compare first half vs second half)
        mid = len(attempts) // 2
        if mid > 0:
            first_half_avg = sum(a.score or 0 for a in attempts[mid:]) / (len(attempts) - mid)
            second_half_avg = sum(a.score or 0 for a in attempts[:mid]) / mid
            if second_half_avg > first_half_avg + 0.05:
                trend = "improving"
            elif second_half_avg < first_half_avg - 0.05:
                trend = "declining"
            else:
                trend = "stable"
        else:
            trend = "stable"
        
        # Find weak and strong topics
        topic_avgs = {t: sum(s) / len(s) for t, s in topic_scores.items()}
        sorted_topics = sorted(topic_avgs.items(), key=lambda x: x[1])
        
        weak_topics = [t for t, s in sorted_topics if s < 0.7][:3]
        strong_topics = [t for t, s in sorted_topics if s >= 0.8][:3]
        
        return {
            "attempts": attempt_data[:10],
            "avg_score": avg_score,
            "trend": trend,
            "weak_topics": weak_topics,
            "strong_topics": strong_topics,
        }
    
    def _analyze_resource_engagement(
        self,
        resources: List[GeneratedResource],
        events: List[LearningEvent],
    ) -> Dict[str, Any]:
        """Analyze resource consumption patterns."""
        by_type: Dict[str, int] = {}
        
        for r in resources:
            rtype = r.resource_type or "unknown"
            by_type[rtype] = by_type.get(rtype, 0) + 1
        
        # Also count from events
        for e in events:
            if e.event_type == "resource_consumed" and e.event_data:
                rtype = e.event_data.get("resource_type", "unknown")
                by_type[rtype] = by_type.get(rtype, 0) + 1
        
        total = sum(by_type.values())
        preferred = max(by_type.items(), key=lambda x: x[1])[0] if by_type else "none"
        
        return {
            "by_type": by_type,
            "total": total,
            "preferred": preferred,
        }
    
    def _analyze_tutor_sessions(self, sessions: List[ChatSession], total_message_count: int = 0) -> Dict[str, Any]:
        """Analyze tutor session patterns."""
        if not sessions:
            return {"count": 0, "avg_messages": 0, "topics": []}
        
        # Use pre-calculated message count to avoid lazy loading
        avg_messages = round(total_message_count / len(sessions), 1) if sessions else 0
        
        # Extract topics from session titles or current_node_id (these are already loaded)
        topics = []
        for s in sessions:
            if s.title and s.title != "New Chat":
                topics.append(s.title)
            elif s.current_node_id:
                topics.append(s.current_node_id)
        
        # Get most common topics
        topic_counts: Dict[str, int] = {}
        for t in topics:
            topic_counts[t] = topic_counts.get(t, 0) + 1
        
        common_topics = sorted(topic_counts.items(), key=lambda x: x[1], reverse=True)[:3]
        
        return {
            "count": len(sessions),
            "avg_messages": avg_messages,
            "topics": [t for t, _ in common_topics],
        }
    
    def _calculate_progress_metrics(
        self,
        attempts: List[QuizAttempt],
        path: Optional[LearningPath],
    ) -> Dict[str, Any]:
        """Calculate progress and streak metrics."""
        # Completion percentage
        completion_pct = 0
        if path and path.path_sequence:
            total_nodes = len(path.path_sequence)
            completed_nodes = len(attempts)  # Simplified
            completion_pct = round((completed_nodes / total_nodes) * 100, 1) if total_nodes > 0 else 0
        
        # Streak calculation
        if not attempts:
            return {
                "completion_pct": completion_pct,
                "current_streak": 0,
                "longest_streak": 0,
                "days_inactive": 999,
            }
        
        dates = set()
        for a in attempts:
            if a.completed_at:
                dates.add(a.completed_at.date())
        
        if not dates:
            return {
                "completion_pct": completion_pct,
                "current_streak": 0,
                "longest_streak": 0,
                "days_inactive": 999,
            }
        
        sorted_dates = sorted(dates, reverse=True)
        last_date = sorted_dates[0]
        today = datetime.now(timezone.utc).date()
        days_inactive = (today - last_date).days
        
        # Current streak
        current_streak = 0
        check_date = today
        while check_date in dates or (check_date == today and days_inactive <= 1):
            if check_date in dates:
                current_streak += 1
            check_date -= timedelta(days=1)
        
        # Longest streak
        longest_streak = 1
        current_longest = 1
        for i in range(1, len(sorted_dates)):
            if (sorted_dates[i - 1] - sorted_dates[i]).days == 1:
                current_longest += 1
                longest_streak = max(longest_streak, current_longest)
            else:
                current_longest = 1
        
        return {
            "completion_pct": completion_pct,
            "current_streak": current_streak,
            "longest_streak": longest_streak,
            "days_inactive": days_inactive,
        }
    
    def _make_aware(self, dt: datetime) -> datetime:
        """Ensure datetime is timezone-aware (UTC)."""
        if dt.tzinfo is None:
            return dt.replace(tzinfo=timezone.utc)
        return dt
    
    def _calculate_learning_velocity(
        self,
        attempts: List[QuizAttempt],
        path: Optional[LearningPath],
    ) -> Dict[str, Any]:
        """Calculate learning velocity and estimate completion."""
        now = datetime.now(timezone.utc)
        
        # Count completions in last 7 and 30 days
        last_7_days = sum(
            1 for a in attempts
            if a.completed_at and (now - self._make_aware(a.completed_at)).days <= 7
        )
        last_30_days = sum(
            1 for a in attempts
            if a.completed_at and (now - self._make_aware(a.completed_at)).days <= 30
        )
        
        # Estimate completion
        estimated_days = None
        if path and path.path_sequence and last_7_days > 0:
            remaining_nodes = len(path.path_sequence) - len(attempts)
            if remaining_nodes > 0:
                weekly_rate = last_7_days
                estimated_days = int((remaining_nodes / weekly_rate) * 7) if weekly_rate > 0 else None
        
        return {
            "last_7_days": last_7_days,
            "last_30_days": last_30_days,
            "estimated_days": estimated_days,
        }
    
